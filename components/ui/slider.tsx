const LogoSlide = ({
  svgContent = <svg width="300" height="200" />,
  logoName,
  isSmallScreen,
}: LogoSlideProps): ReactElement => {
  const { theme, setTheme } = useTheme();
  const [hasPngVariants, setHasPngVariants] = useState<boolean>(false);
  const [mounted, setMounted] = useState(false);
  const router = useRouter();

  useEffect(() => {
    const testImage = document.createElement("img");
    testImage.src = `/images/partners-logos/${logoName}_${theme}.png`;

    testImage.onload = () => setHasPngVariants(true);
    testImage.onerror = () => setHasPngVariants(false);
    setMounted(true);
  }, [logoName, theme]);

  const handleClick = () => {
    router.push("/portfolio");
  };

  return (
    <div
      onClick={handleClick}
      className={`flex justify-center items-center px-10 light:text-[var(--gray-100)] text-[var(--gray-0)] ${
        isSmallScreen ? "" : ""
      }`}
    >
      {mounted && hasPngVariants ? (
        <Image
          priority
          src={`/images/partners-logos/${logoName}_${theme}.png`}
          alt={`${logoName} Logo`}
          width={300}
          height={200}
          className="object-contain w-full h-16 md:h-20"
        />
      ) : (
        svgContent
      )}
    </div>
  );
};

